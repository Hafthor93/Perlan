/*
Create a function called CardNamer.

When given an integer between 1-13,
it should return the name of the corresponding card (Ignore the suit)
 */

String CardNamer(int i){
  if(i == 1){
    return "A";
  } else if (i == 2){
    return "Tvistur";
  } else if (i == 3){
    return "Þristur";
  } else if (i == 4){
    return "Fjarki";
  } else if (i == 5){
    return "Fimma";
  } else if (i == 6){
    return "Sexa";
  } else if (i == 7){
    return "Sjöa";
  } else if (i == 8){
    return "Átta";
  } else if (i == 9){
    return "Nía";
  } else if (i == 10){
    return "Tía";
  } else if (i == 11){
    return "J";
  } else if (i == 12){
    return "Q";
  } else if (i == 13){
    return "K";
  }





}